import java.io.*;
class prog2NumDisp
{
  public static void main(String args[])
  {
	int a=2,b=4;
	System.out.println(a+b);	//6
	System.out.println(""+a+b);	//24
	System.out.println("sum = " + a+b); //sum=24
	System.out.println("a = " + a + " b = " + b);
	//a = 2 b = 4
	System.out.println("sum = " + (a+b));//sum=6
  }
}